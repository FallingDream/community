package com.wang.community.dto;

public class GithubUser {
    private String name;
    private long id;
    private String boi;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getBoi() {
        return boi;
    }

    public void setBoi(String boi) {
        this.boi = boi;
    }
}
